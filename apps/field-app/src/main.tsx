import "./index.css";
import { StrictMode } from "react";
import { createRoot } from "react-dom/client";
import { registerSW } from "virtual:pwa-register";
import { flush, getDeviceId } from "@prehospital-ems/sync-engine";
import { loadRuntimeConfig } from "./config.js";
import { isEnrolled, refreshDeviceConfig } from "./provisioning.js";
import { applyDocumentLocale } from "./i18n/index.js";
import { I18nProvider } from "./i18n/react.js";
import { App } from "./App.js";

// The PHI at-rest key and app-lock state are provisioned by App via initAppLock:
// in device-key mode it installs the key immediately; once a PIN is set the app
// stays locked until it's entered. Database operations queue on the key gate, so
// a read firing before unlock never races. (App lock — issue #2.)

// Register the service worker (generated by vite-plugin-pwa).
// Auto-update on new deploy — no user prompt needed for a field tool.
registerSW({ immediate: true });

// Listen for FLUSH messages posted by the service worker's Background Sync
// handler — fires when the device reconnects after being offline.
if ("serviceWorker" in navigator) {
  navigator.serviceWorker.addEventListener("message", (event: MessageEvent<{ type: string }>) => {
    if (event.data?.type === "FLUSH") {
      void flush();
    }
  });
}

const root = document.getElementById("root");
if (!root) throw new Error("No #root element");

// Set <html lang dir> from the saved/device locale before the first paint so RTL
// languages (issue #16) lay out correctly from the very first frame.
applyDocumentLocale();

// Resolve the per-facility runtime config (issue #14) before the first render so
// the OpenMRS base URL and location/concept UUIDs are correct for login and the
// first capture. Offline, this falls back to the cached config applied at module
// load — boot never blocks on the network.
void loadRuntimeConfig()
  .then(async () => {
    // Fleet config push (issue #15): a device enrolled with a provisioning service
    // pulls its latest config on boot, so ops can re-point the fleet centrally.
    // Fast no-op when unenrolled; offline it keeps the cached provisioned layer.
    if (!isEnrolled()) return;
    try {
      await refreshDeviceConfig({ deviceId: await getDeviceId() });
    } catch { /* boot never blocks on provisioning */ }
  })
  .finally(() => {
    createRoot(root).render(
      <StrictMode>
        <I18nProvider>
          <App />
        </I18nProvider>
      </StrictMode>,
    );
  });
